class BSTIterator {
public:
	stack<TreeNode *>st;
	BSTIterator(TreeNode *root) {
		while (root != NULL) {
			st.push(root);
			root = root->left;
		}
	}

	/** @return whether we have a next smallest number */
	bool hasNext() {
		return !st.empty();
	}

	/** @return the next smallest number */
	int next() {
		TreeNode * tmp = st.top();
		st.pop();
		int ret = tmp->val;
		if (tmp->right != NULL) {
			tmp = tmp->right;
			while (tmp) {
				st.push(tmp);
				tmp = tmp->left;
			}
		}
		return ret;
	}
};

